import { copyFile, mkdir, readdir, rm, stat, writeFile } from "node:fs/promises";
import { dirname, join, relative, sep } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const distDir = join(repoRoot, "web/dist");
const outputPath = join(repoRoot, "src/embedded_assets.zig");
const embedDir = join(repoRoot, "src/embedded_asset_files");

function zigString(value) {
  return JSON.stringify(value).replace(/\u2028/g, "\\u2028").replace(/\u2029/g, "\\u2029");
}

function contentType(path) {
  if (path.endsWith(".html")) return "text/html; charset=utf-8";
  if (path.endsWith(".js")) return "text/javascript; charset=utf-8";
  if (path.endsWith(".css")) return "text/css; charset=utf-8";
  if (path.endsWith(".svg")) return "image/svg+xml";
  if (path.endsWith(".json")) return "application/json";
  return "application/octet-stream";
}

async function listFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await listFiles(fullPath)));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }
  return files.sort();
}

const files = await listFiles(distDir);
const assets = [];
await rm(embedDir, { force: true, recursive: true });
for (const file of files) {
  const rel = relative(distDir, file).split(sep).join("/");
  const route = `/${rel}`;
  const copiedPath = join(embedDir, rel);
  await mkdir(dirname(copiedPath), { recursive: true });
  await copyFile(file, copiedPath);
  const embedPath = relative(join(repoRoot, "src"), copiedPath).split(sep).join("/");
  const size = (await stat(file)).size;
  assets.push({ route, embedPath, contentType: contentType(route), size });
  if (route === "/index.html") {
    assets.push({ route: "/", embedPath, contentType: contentType(route), size });
  }
}

const body = `// Generated by scripts/generate-embedded-assets.mjs. Do not edit.
pub const Asset = struct {
    path: []const u8,
    content_type: []const u8,
    body: []const u8,
};

pub const assets = [_]Asset{
${assets
  .map(
    (asset) =>
      `    .{ .path = ${zigString(asset.route)}, .content_type = ${zigString(asset.contentType)}, .body = @embedFile(${zigString(asset.embedPath)}) }, // ${asset.size} bytes`,
  )
  .join(",\n")}
};
`;

await writeFile(outputPath, body);
